package com.mindor.entity;

public class ShareRecord {
	private int ShareRecordId;
	private String phone;
	private String userIdRoot;// 主权限用户id
	private String userId;// 次权限用户id
	private String userHead;// 次权限用户头像
	private String equipmentId;

	public String getUserIdRoot() {
		return userIdRoot;
	}

	public void setUserIdRoot(String userIdRoot) {
		this.userIdRoot = userIdRoot;
	}

	public String getUserHead() {
		return userHead;
	}

	public void setUserHead(String userHead) {
		this.userHead = userHead;
	}

	public int getShareRecordId() {
		return ShareRecordId;
	}

	public void setShareRecordId(int shareRecordId) {
		ShareRecordId = shareRecordId;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getEquipmentId() {
		return equipmentId;
	}

	public void setEquipmentId(String equipmentId) {
		this.equipmentId = equipmentId;
	}

}
